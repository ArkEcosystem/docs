# Updating Projects on MarketSquare

MarketSquare allows Projects to be updated by members with administrator access. The process is similar to Project creation, but with a few minor differences. If you have not already created a Project, please visit the [Project Creation](/docs/marketsquare/projects/creating-projects) page.

## Getting Started

To get started updating an existing project, navigate to its listing page and click '**Edit Project**'.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-edit-focus.png)

---

We can also select '**My Projects**' from the drop-down menu of a Project admin or team member account.

![](/storage/docs/docs/marketsquare/assets/projects/projects-my-focus.png)

![](/storage/docs/docs/marketsquare/assets/projects/projects-my.png)

---

Next, click the '**Edit**' button next to the relevant Project, then select '**Edit Project**'.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-edit-project-focus.png)

## The Project's Update Page

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-overview.png)

---

### Platforms

Here, we can select up to 5 different platforms related to our listing. Platforms may be chosen from the provided list, or a custom platform can be added as well.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-platforms.png)

---

To enter a custom Platform, click '**Add Platform**', type in the desired platform, and click '**Save**'.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-platforms-2.png)

<x-alert type="info">
For more information about supported or custom platforms, please visit: **[Supported Assets: Platforms](/docs/marketsquare/support/supported-assets#platforms)**
</x-alert>

---

### Category

Categories summarize the key topics surrounding a Project and help to explain its purpose. Only 1 category may be selected.

Some examples include, '**Games**', '**Developer Tools**', or '**Storage**'.

After making a selection, click the ‘Save’ button to confirm the change.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-category.png)

<x-alert type="info">
For more information about supported categories, please visit: **[Supported Assets: Categories](/docs/marketsquare/support/supported-assets#categories)**
</x-alert>

---

### Tags

These can be anything that could help a user find or understand what a particular project is about, but are somewhat more specific and less hierarchical than a category or platform.

Type the desired tags into the Tags form, and click the ‘Save’ button to confirm the changes.

Up to 3 tags may selected.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-tags.png)

<x-alert type="info">
For more information about Tags, please visit: **[Supported Assets: Tags](/docs/marketsquare/support/supported-assets#tags)**
</x-alert>

## Project Information

In this section, we'll be asked to update the Project's basic information as well as screenshots and links to videos social media accounts and repositories.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-info.png)

---

### Project Name

This is the name presented across all of MarketSquare, and will be visible in related searches and categories.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-name.png)

<x-alert type="info">
A Project name should be no more than 30 characters
</x-alert>

---

### Website

This can be any important links we'd like to update for our listing, such as [https://explorer.ark.io](https://explorer.ark.io/).

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-website.png)

<x-alert type="info">
All website links must begin with `http://` or `https://`
</x-alert>

---

### Avatar

Avatars are optional, but highly recommended as they are the face of a Project.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-avatar.png)

<x-alert type="info">
For more information about Avatars, please visit: **[Supported Assets: Images](/docs/marketsquare/support/supported-assets#images)**
</x-alert>

### Description

The Project's description--just as in a MarketSquare Profile--supports markdown and is limited to 1,500 characters.

This is where we'll want to update important information about our listing.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-description.png)

---

### Screenshots

This is a chance to show MarketSquare users what a Project looks like in action.

Up to 8 screenshots can be added to a Project listing.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-screenshots.png)

<x-alert type="info">
For more information about Screenshots, please visit: **[Supported Assets: Images](/docs/marketsquare/support/supported-assets#images)**
</x-alert>

---

### Videos

Have a new YouTube link explaining more about a Project? Update them here to give MarketSquare users a fresh view of our Project.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-videos.png)

<x-alert type="info">
For more information about Video links, please visit: **[Supported Assets: Videos](/docs/marketsquare/support/supported-assets#videos)**
</x-alert>

---

### Social Media Links

Update any Project-related social media links here to engage users on and off MarketSquare.

This includes platforms such as Twitter, Discord, Facebook, Reddit, and more!

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-social-media.png)

<x-alert type="info">
For more information about Social Media links, please visit: **[Supported Assets: Social Media](/docs/marketsquare/support/supported-assets#social-media)**
</x-alert>

---

### Repositories

New code repo? Update it here to show off the current progress!

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-repository.png)

<x-alert type="info">
For more information about Repository link, please visit: **[Supported Assets: Repositories](/docs/marketsquare/support/supported-assets#repositories)**
</x-alert>

## Project Preview

On this page, we'll be presented with an overview of our updated MarketSquare Project and be able to '**Cancel**', '**Edit**', or ‘**Save**’ our listing updates.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-preview.png)

---

If everything looks good, click '**Save**' to complete the update process, or click '**Cancel**' to discard all changes.

![](/storage/docs/docs/marketsquare/assets/projects/projects-update-save.png)

<x-alert type="info">
Congratulations! Your Project has been listed on MarketSquare!
</x-alert>
