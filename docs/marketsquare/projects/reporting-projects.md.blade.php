# Reporting Projects on MarketSquare

Reporting a Project on MarketSquare that is in violation of the Terms of Service helps our moderation team and ensures a positive experience for all users.

---

Some Projects may contain spam or harmful content that violates the Terms of Service. Those reviews can be reported for abuse within MarketSquare.

Content that should be reported includes, but is not limited to, the following:

- Sexual material
- Violent or repulsive scenes
- Insults or expressions of intolerance
- Harmful or dangerous actions
- Spam or misleading
- Abusive or harassing

## The ‘Report’ Icon

Find the Project to be reported, and click the icon as shown below.

![](/storage/docs/docs/marketsquare/assets/projects/projects-report.png)

## Provide Report Details

After clicking the '**Report**' icon, a form will open which allows selecting the kind of abuse being reported. Once a reason is chosen, more information can be added in the message box.

![](/storage/docs/docs/marketsquare/assets/projects/report-modal.png)

Once the report is complete, submit it by clicking the '**Save**' button on the bottom right of the form. The report will then be sent to the MarketSquare moderation team.

If the Project is found to be in violation of MarketSquare’s Terms of Service, it will be removed.
