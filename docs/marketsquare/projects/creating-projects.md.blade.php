# Creating Projects on MarketSquare

MarketSquare allows users to list Projects such as Businesses, Network Validators, and Plugins.

Whether you're a developer offering freelance services, a small team with a groundbreaking new dApp, or a multi-billion-dollar ecosystem searching for your next hire, MarketSquare is a platform for everything Blockchain.

## Getting Verified

Before you jump in and add your project you'll need to verify your project. Click on the '**Add Project**' button at the top of the MarketSquare Homepage to get started.

![](/storage/docs/docs/marketsquare/assets/projects/projects-add.png)

---

You'll then be presented with the Project Verification page. Fill out the form in its entirety and we will review the information and get back to you.

![](/storage/docs/docs/marketsquare/assets/projects/projects-verification.png)

<x-alert type="warning">
Please refrain from registering a project on MarketSquare if you are not its owner or authorized representative. If you would like to see your favorite project on MSQ, direct them to the submission form above or have them contact [ray@ark.io](mailto:ray@ark.io).
</x-alert>

## Getting Started

Once you've been verified, you are ready to add your project! Click the '**Add Project**’ button at the top of the MarketSquare Homepage.

![](/storage/docs/docs/marketsquare/assets/projects/projects-add.png)

## The Project Registration Page

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration.png)

---

### Platforms

Here, we can select up to 5 different platforms related to our new listing. Platforms can be chosen from the provided list, or a custom platform may also be added.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-platforms.png)

---

To enter a custom Platform, click '**Add Platform**', type in the desired platform, and click '**Save**'.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-platforms-2.png)

<x-alert type="info">
For more information about supported or custom platforms, please visit: **[Supported Assets: Platforms](/docs/marketsquare/support/supported-assets#platforms)**
</x-alert>

---

### Category

Categories summarize the key topics surrounding a Project and help to explain its purpose. Only 1 category may be selected.

Some examples include, '**Games**', '**Developer Tools**', or '**Storage**'.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-category.png)

<x-alert type="info">
For more information about supported categories, please visit: **[Supported Assets: Categories](/docs/marketsquare/support/supported-assets#categories)**
</x-alert>

---

### Tags

These can be anything that could help a user find or understand what a particular project is about, but are somewhat more specific and less hierarchical than a category or platform.

Type the desired tags into the Tags form, and click the enter key on your keyboard.

Up to 3 tags may selected.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-tags.png)

<x-alert type="info">
For more information about Tags, please visit: **[Supported Assets: Tags](/docs/marketsquare/support/supported-assets#tags)**
</x-alert>

---

## Basic Information

In this section, we'll be asked to enter the Project's basic information as well as screenshots and links to videos social media accounts and repositories.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-info.png)

---

### Project Name

This is the name presented across all of MarketSquare. This will be visible in relevant searches and categories.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-name.png)

<x-alert type="info">
A Project name should be no more than 30 characters
</x-alert>

---

### Website

This can be any important link associated with our new listing, such as [https://ark.io](https://ark.io/).

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-website.png)

<x-alert type="info">
All website links must begin with `http://` or `https://`
</x-alert>

---

### Avatar

Avatars are optional, but highly recommended as they are the face of a Project.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-avatar.png)

<x-alert type="info">
For more information about Avatars, please visit: **[Supported Assets: Images](/docs/marketsquare/support/supported-assets#images)**
</x-alert>

---

### Description

The Project's description--just as in a MarketSquare Profile--supports markdown and is limited to 1,500 characters.

This is where we'll want to highlight important information about our new listing.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-description.png)

---

### Screenshots

This is a chance to show MarketSquare users what a Project looks like in action.

Up to **8** screenshots can be added to a Project listing.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-screenshots.png)

<x-alert type="info">
For more information about Screenshots, please visit: **[Supported Assets: Images](/docs/marketsquare/support/supported-assets#images)**
</x-alert>

---

### Videos

Have a YouTube link explaining more about a Project? Enter it here to give MarketSquare users a more in-depth look.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-videos.png)

<x-alert type="info">
For more information about Video links, please visit: **[Supported Assets: Videos](/docs/marketsquare/support/supported-assets#videos)**
</x-alert>

---

### Social Media Links

Enter any Project-related social media links here to engage users on and off MarketSquare.

This includes platforms such as Twitter, Discord, Facebook, Reddit, and more!

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-social-media.png)

<x-alert type="info">
For more information about Social Media links, please visit: **[Supported Assets: Social Media](/docs/marketsquare/support/supported-assets#social-media)**
</x-alert>

---

### Repositories

Does this Project feature code? Share the repository here to show off the hard work!

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-repository.png)

<x-alert type="info">
For more information about Repository link, please visit: **[Supported Assets: Repositories](/docs/marketsquare/support/supported-assets#repositories)**
</x-alert>

---

### Agree to the Terms of Service

Once satisfied with a Project's details, read, then agree to the Terms of Service by checking the '*I affirm...*' box and click '**Continue**'.

![](/storage/docs/docs/marketsquare/assets/projects/projects-confirm.png)

## Project Preview

On this page, we'll be presented with an overview of our new MarketSquare Project and be able to '**Cancel**', '**Edit**', or ‘**Save**’ the new listing.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-preview.png)

---

If everything looks good, click '**Save**' to complete the process.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-save.png)

<x-alert type="info">
Congratulations! Your Project has been listed on MarketSquare!
</x-alert>

---

If you'd like to cancel adding a Project, click "**Cancel**', then confirm your choice to discard all changes by clicking '**Confirm**'.

![](/storage/docs/docs/marketsquare/assets/projects/projects-registration-cancel.png)
