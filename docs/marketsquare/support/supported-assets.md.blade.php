### Tags

Tags are another excellent way to increase the overall visibility of Projects on MarketSquare.

These can be anything that could help a user find or understand what a particular project is about, but are somewhat more specific _and_ less hierarchical than a category or platform.

Some common examples include tags like, "**startup**, **open source**, **nft**, or **defi**".

