## Hub Announcements

Following a Hub and receiving announcements on MarketSquare is simple and allows users to receive [notifications](/docs/marketsquare/notifications/handling-notifications) about the Hub's latest news and updates.

### Following Hubs

To follow a Hub and receive announcements, navigate to the desired Hub and click the ‘**+ Follow**’ button to the right on the Hub’s page.

![](/storage/docs/docs/marketsquare/assets/getting-started/hubs-follow.png)

---

We can confirm the Hub is being followed when that same button shows as ‘**Following**’.

![](/storage/docs/docs/marketsquare/assets/getting-started/hubs-following.png)

The following is an example of what a Hub announcement might look like. As discussed, these announcements are notifications sent by Hubs we’ve chosen to follow.

![](/storage/docs/docs/marketsquare/assets/notifications/notifications-announcement-focus.png)

---

### Unsubscribing to Hub Announcements

If we no longer want to receive announcements from a particular Hub, we can navigate to that Hub and click the ‘**Following**’ button to the right on the Hub’s page.

![](/storage/docs/docs/marketsquare/assets/getting-started/hubs-following.png)

---

We can confirm the Hub is no longer being followed when that same button gives us the option to ‘**+ Follow**’.

![](/storage/docs/docs/marketsquare/assets/getting-started/hubs-follow.png)
