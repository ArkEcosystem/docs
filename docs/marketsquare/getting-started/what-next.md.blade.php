# What Can I Do Next?

---

<ins>_**I created an Account\, now what?**_</ins>

---

After [creating an Account](/docs/marketsquare/getting-started/account-sign-up) on MarketSquare, you can [customize your profile](/docs/marketsquare/profiles/updating-profile), explore the growing list of Projects & Hubs, keep up with the latest news and announcements, [add your own Projects](/docs/marketsquare/projects/creating-projects), and interact with others in the blockchain community! Make sure to update [your settings](/docs/marketsquare/profiles/settings) to secure your account too!

<!-- markdownlint-disable MD026 -->
## Customize Your Profile!
<!-- markdownlint-enable MD026 -->

[Customizing your Profile](/docs/marketsquare/profiles/updating-profile) on MarketSquare is easy. Change your username, upload a personal avatar, and share more about yourself with a personal bio or website, social media and repository links.

![](/storage/docs/docs/marketsquare/assets/profiles/profiles-overview.png)

<!-- markdownlint-disable MD026 -->
## Discover Great Projects!
<!-- markdownlint-enable MD026 -->
Take some time to explore MarketSquare and all the great projects that are joining each day. The MarketSquare homepage features trending projects, categories where you can search for specific projects, news, and some of the top games on our platform. You can even share your experience with a given Project by [writing a review](/docs/marketsquare/reviews/writing-reviews)!

![](/storage/docs/docs/marketsquare/assets/getting-started/menu-discover.png)

<!-- markdownlint-disable MD026 -->
## Create your Own Project!
<!-- markdownlint-enable MD026 -->

If you represent a project and would like to have your project on MarketSquare, you can easily get started by [creating your project](/docs/marketsquare/projects/creating-projects) page.

![](/storage/docs/docs/marketsquare/assets/projects/projects-add.png)

During this process, you can add all the pertinent information about your projects, link to social media accounts, repositories, and upload videos and screenshots if applicable. After you have completed your project, you can always go back at any time to make [edits](/docs/marketsquare/projects/updating-projects) or [remove](/docs/marketsquare/projects/removing-projects) the project if necessary.

<!-- markdownlint-disable MD026 -->
## Build your Team!
<!-- markdownlint-enable MD026 -->

If you created a project you may notice that you are the sole team member. Make sure to [invite](/docs/marketsquare/teams/inviting-members) the rest of your team to not only celebrate your projectâ€™s team but to make [managing](/docs/marketsquare/teams/managing-members) your project on MarketSquare easier.

![](/storage/docs/docs/marketsquare/assets/teams/teams-invite.png)

The greatest resource behind any project is their team members. Team members can be invited to join projects, list their job title, and be given roles that outline the actions they can take on a project page.

<!-- markdownlint-disable MD026 -->
## Connect with Hubs!
<!-- markdownlint-enable MD026 -->

At MarketSquare, [Hubs](/docs/marketsquare/overview/marketsquare-hubs) are your gateway to entire ecosystems. They will be the focal point of entire networks where you can check out featured projects, educational videos, news, and more!

Get started by visiting the ARK & Hive Hubs! Don't forget to follow them so you can stay up to date and receive [announcements](/docs/marketsquare/notifications/hub-announcements)!

![](/storage/docs/docs/marketsquare/assets/getting-started/msq-hubs.png)

As MarketSquare continues to grow we will continually add more Hubs based on feedback and community support.

