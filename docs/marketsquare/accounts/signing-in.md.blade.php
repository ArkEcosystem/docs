# Signing into an Account

## Getting Started

To get started, click the **'Sign In'** button at the top right of the MarketSquare Homepage.

![](/storage/docs/docs/marketsquare/assets/accounts/accounts-sign-in-focus.png)

## The MarketSquare Sign In Page

Enter your account's Email or Username and Password, then click the **'Sign In'** button.

![](/storage/docs/docs/marketsquare/assets/accounts/accounts-sign-in-page.png)
