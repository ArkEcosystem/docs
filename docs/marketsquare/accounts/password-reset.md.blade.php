# Resetting a MarketSquare Account's Password

An account with a lost or forgotten password can be recovered by using your email address to reset the password.

## Getting Started

To get started, click the 'Sign In' button at the top right of the MarketSquare Homepage.

## The MarketSquare Sign In Page

Click the **'[Forgot password?](https://marketsquare.io/forgot-password)'** link below the **'Password'** field.

## The Password Reset Page

Enter your email address, then click the **'Send Reset Link'** button.

<x-alert type="info">
Make sure to provide the email address currently associated with your account.
</x-alert>

