# Inviting Team Members to MarketSquare

MarketSquare enables users with admin privileges to invite others to join their team. Inviting a team member can be done directly on MarketSquare with just their email address.

## Getting Started

Get started by clicking the Avatar at the top of the page and selecting ‘**My Projects**’ from the drop-down menu.

![](/storage/docs/docs/marketsquare/assets/projects/projects-my-focus.png)

---

### Select ‘Manage Team’

From the ‘**My Projects**’ page, click the ‘Edit’ button of the desired Project and select "**Manage Team**" from the pop-up menu.

![](/storage/docs/docs/marketsquare/assets/projects/projects-my-edit.png)

---

### Click Invite Button

In order to invite new members to a team, click the ‘**Invite**’ button at the top right of the page.

![](/storage/docs/docs/marketsquare/assets/teams/teams-invite.png)

---

### Enter Details

After clicking ‘**Invite**’, enter the team member information in the pop-up window, fill in the members information, then click the ‘**Send**’ button.

- **Name**: the name of the new Team Member to invite
- **Email**: the new Team Members email address
- **Role**: determines their project permissions
    1. **Admin**: An admin ***can*** add and remove team members, respond to reviews, and manage most aspects of a project. An admin ***cannot*** remove the owner of a project
    2. **Contributor**: A contributor ***can*** respond to reviews as a member of a project. A contributor ***cannot*** manage the team members or admin functions of a project

![](/storage/docs/docs/marketsquare/assets/teams/teams-member-info.png)

## Success

A window verifying the invitation was sent will be shown indicating the process was successful.

![](/storage/docs/docs/marketsquare/assets/teams/teams-success.png)

## View Pending Invitations

Once completed, a new pending invitation will be shown for the new team members that have been invited.

Now, simply let the invitee know to check their email, accept the invitation and sign up for MarketSquare.

![](/storage/docs/docs/marketsquare/assets/teams/teams-pending.png)

<x-alert type="success">
You have just successfully invited team members to your Project on MarketSquare!
</x-alert>

## View My Invitations

Pending invitations can be viewed by clicking the '**Invitations**' button at the top of the page (as shown below).

Choose ‘**Accept**’ or ‘**Decline**’ from this drop-down menu, or click "**Show All**" to view the entire list.

![](/storage/docs/docs/marketsquare/assets/teams/teams-my-invitations.png)

---

We can also decline an invitation from the '**Invitations**' page

![](/storage/docs/docs/marketsquare/assets/teams/teams-decline.png)
