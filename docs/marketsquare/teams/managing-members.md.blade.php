# Managing Team Members on MarketSquare

One of the biggest points of attraction for using MarketSquare is team management. With MarketSquare, users can showcase which Projects they have been involved with and discover other users through browsing various Projects on the platform. Managing teams is simple within MarketSquare, and if help is needed, refer to the guides in this section.

## Getting Started

After signing in and authenticating an account, click the **Account Menu** dropdown in the upper right portion of the screen and choose **My Projects**.

![](/storage/docs/docs/marketsquare/assets/projects/projects-my-focus.png)

---

### Click ‘Edit’ to Manage Team

Next to the Project we’d like to update, click the ‘**Edit**‘ icon and select ‘**Manage Team**’ from the popup menu.

![](/storage/docs/docs/marketsquare/assets/projects/projects-my.png)

---

### Team Member Section

This is where we can manage team member invitations, roles, and removal.

![](/storage/docs/docs/marketsquare/assets/teams/teams-projects-overview.png)

---

### Click Edit Team Member

Next to the Team Member we'd like to add details for, click the '**Edit**' icon.

![](/storage/docs/docs/marketsquare/assets/teams/teams-edit-member-focus.png)

---

### Add Details

Add **Job**, **Title**, and **Contact Details** for the Team Member.

![](/storage/docs/docs/marketsquare/assets/teams/teams-edit-member.png)

---

### Click ‘Save’

Click the '**Save**' button to save changes.

![](/storage/docs/docs/marketsquare/assets/teams/teams-save-member.png)

<x-alert type="success">
You have now successfully edited details for Team Members in your Project!
</x-alert>

## Removing Team Members

If for some reason we need to remove a collaborator from a listing, we can do that by following this guide.

<x-alert type="info">
As a Project listing registrant, you can remove Team Members from your Project in just a few steps
</x-alert>

---

### Access My Projects

After signing in and authenticating an account, click the Avatar at the upper right portion of the screen and choose '**My Projects**'.

![](/storage/docs/docs/marketsquare/assets/projects/projects-my-focus.png)

---

### Click ‘Edit’ to Manage Team

Next to the Project we’d like to update, click the ‘**Edit**‘ icon which will open a popup menu.

From this popup menu, select ‘**Manage Team**’.

![](/storage/docs/docs/marketsquare/assets/projects/projects-my-edit.png)

---

### Team Member Section

The list of current Project team members will be visible towards the bottom of the page.

![](/storage/docs/docs/marketsquare/assets/teams/teams-members-project.png)

---

### Click the ‘Delete’ Icon

Click the ‘**Delete**’ icon next to the member we’d like to remove.

![](/storage/docs/docs/marketsquare/assets/teams/teams-remove-member-focus.png)

---

### Confirm Removal

Click ‘**Yes, I’m Sure**’ to confirm member removal.

![](/storage/docs/docs/marketsquare/assets/teams/teams-delete-confirm.png)

<x-alert type="danger">
While removing a team member is permanent, you will be required to send a new invitation before the user can re-join the team.
</x-alert>

---

From this page, we may also leave a team.

![](/storage/docs/docs/marketsquare/assets/teams/teams-leave.png)
