### Block Height

Integer used to determine the specific order of a block in a Blockchain. If a chain contains 10 blocks, the oldest block has a height of 1 and the newest block has a height of 10. [Watch Video](https://youtu.be/0r4dvEUJtpw)

**More:** [Blockchain](#blockchain)

### Block ID

Alphanumeric string used to uniquely identify each block in a Blockchain.

**More:** [Blockchain](#blockchain)

### Block Propagation

The process of a newly generated and valid block being distributed to all other nodes on a Blockchain network.

**More:** [Blockchain](#blockchain), [Network](#network), [Node](#node)

### Block Reward

Payment of a cryptoasset from the network to the miner or validator that creates a new block and adds it to the Blockchain. Usually generated as new coins that are added to circulation. [Watch Video](https://youtu.be/PNwcBv8lHDU)

**More:** [Cryptoasset](#cryptoasset), [Blockchain](#blockchain), [Validator](#validator), [Network](#network), [Coin](#coin)

### Block Size

How large each block of the Blockchain is, usually denoted in megabytes. [Watch Video](https://youtu.be/I-rGq0ya7dw)

**More:** [Blockchain](#blockchain)

### Block Time

How long each block takes to be added to the Blockchain. The block time of Bitcoin aims to be 10 minutes, and for the ARK Public Network 8 seconds. [Watch Video](https://youtu.be/3FJqvBY0L2k)

**More:** [ARK Public Network](#ark-public-network), [Block Time](#block-time), [Blockchain](#blockchain), [Coin](#coin)

### Blockchain

A distributed, immutable ledger where identical copies are maintained in multiple locations known as nodes. A consensus mechanism determines the process of how blocks are added.

**More:** [Consensus Mechanism](#consensus-mechanism), [Ledger](#ledger), [Node](#node)

### Blockchain Trilemma

Refers to the zero-sum relationship between scalability, security, and decentralization when designing a Blockchain. Optimizing for any one property usually results in tradeoffs in the others.

**More:** [Decentralization](#decentralization), [Blockchain](#blockchain)

### Bounty

An award paid via a cryptoasset for contributing to the codebase, disclosing security vulnerabilities, discovering bugs, or some other effort. [Watch Video](https://youtu.be/zfeuee-Hq4A)

**More:** [Cryptoasset](#cryptoasset), [Codebase](#codebase)

### Bridgechain

Deprecated term for an ARK-based chain.

**More:** [ARK-Based Chain](#ark-based-chain)

### Burn (Burning)

Removing tokens or coins from the circulating supply by either sending them to a provably unusable address or removing them as part of the consensus mechanism. [Watch Video](https://youtu.be/V4m2CQvVaYQ)

**More:** [Consensus Mechanism](#consensus-mechanism), [Circulating Supply](#circulating-supply), [Address](#address), [Token](#token), [Coin](#coin)

### Byzantine Fault Tolerance

The ability of a system to withstand failures of its components. Components of the system may discuss among each other and reach consensus on the nature of a faulty component. [Watch Video](https://youtu.be/2c_Z2EMHPnY)

**More:** [Consensus](#consensus)

### Byzantine General's Problem

Problem relating to consensus where members of a group must come to an agreement on a successful strategy while knowing that some members may aim to sabotage the group.

**More:** [Consensus](#consensus)

