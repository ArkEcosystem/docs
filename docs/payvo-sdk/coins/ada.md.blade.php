# Cardano

This is a Cardano Driver for the Platform SDK. The implementation makes use of [Cardano](https://cardano.org/) and adheres to the contracts laid out in the [specification](/docs/payvo-sdk/specification). **This package should not be used directly. It is designed to be consumed through the [SDK](/docs/payvo-sdk/sdk) package.**

