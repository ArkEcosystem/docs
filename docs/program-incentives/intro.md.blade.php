# Introduction

## ARK Grants
ARK Grants is a developer incentive program backed by a dedicated 1 million ARK fund. This program enables developers to get funding for building ARK-powered Proofs-of-Concept, applications, and plugins. Applying is a simple process, and projects are made open source to benefit the developer community.
<livewire:page-reference path="/docs/program-incentives/ark-grants-program-guidelines" />

## Development and Security Bounty Program
Developers, auditors, and bug hunters may earn ARK bounties for discovering and reporting security vulnerabilities and bugs in the ARK codebase. Bounties are handled on a case-by-case basis based on the nature and severity of the discovery. Members of the program do their part to make the ARK codebase secure and robust, and joining the program is simple.
<livewire:page-reference path="/docs/program-incentives/bounty" />

## Contribution Guidelines
Developers are expected to follow contribution guidelines to keep the codebase efficient, readable, and standardized. Contribution guidelines are clearly laid out so developers and contributors can submit their work without much revision, resulting in faster development and more useful contributions. Check out these guidelines before contributing code to the project.
