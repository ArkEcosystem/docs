# Directory Structure

If you're a developer and looking into contributing to ARK Core then this is the right starting point for you to get familiar with how it is structured.

## The `packages` Directory

The `packages` directory contains all of the official packages that are developed and maintained by the ARK Ecosystem development team. You should **never** add your own packages to this directory.

## The `plugins` Directory

The `plugins` directory contains all of the packages that you might be working on locally. You should **always** add your own packages to this directory.

## The `__tests__` Directory

The `__tests__` directory contains all of the tests that you might be working with when preparing a pull requests. You should **never** add tests for your own plugins to this directory.

