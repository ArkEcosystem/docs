# Nodem Requirements

Developers running Nodem should be familiar with—and have installed—the following:

- [PHP 8](https://www.php.net/releases/8.0/en.php)
- [Composer](https://getcomposer.org)
- [Valet](https://laravel.com/docs/5.7/valet) or [Homestead](https://laravel.com/docs/5.7/homestead)
- [PostgreSQL](http://www.postgresql.org) or [MySQL](www.mysql.com)
