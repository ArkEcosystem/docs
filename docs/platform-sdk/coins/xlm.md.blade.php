# Stellar

This is a Stellar Driver for the Platform SDK. The implementation makes use of [Stellar](https://www.stellar.org/) and adheres to the contracts laid out in the [specification](http://docs.ark.io.test/docs/platform-sdk/specification). **This package should not be used directly. It is designed to be consumed through the [SDK](/docs/platform-sdk/sdk) package.**

