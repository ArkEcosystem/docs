# Lisk

This is a Lisk Driver for the Platform SDK. The implementation makes use of [Lisk](https://lisk.io/) and adheres to the contracts laid out in the [specification](/docs/platform-sdk/specification). **This package should not be used directly. It is designed to be consumed through the [SDK](/docs/platform-sdk/sdk) package.**

