# Monero

This is a Monero Driver for the Platform SDK. The implementation makes use of [Monero](https://www.getmonero.org/) and adheres to the contracts laid out in the [specification](/docs/platform-sdk/specification). **This package should not be used directly. It is designed to be consumed through the [SDK](/docs/platform-sdk/sdk) package.**

