# CoinCap

This is a CoinCap Client for the Platform SDK. The implementation makes use of [CoinCap](https://coincap.io/) and adheres to the contracts laid out in the [specification](/docs/platform-sdk/specification). **This package should not be used directly. It is designed to be consumed through the [Markets](/docs/platform-sdk/markets) package.**

