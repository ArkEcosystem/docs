# CoinGecko

This is a CoinGecko Client for the Platform SDK. The implementation makes use of [CoinGecko](https://coingecko.com/) and adheres to the contracts laid out in the [specification](http://docs.ark.io.test/docs/platform-sdk/specification). **This package should not be used directly. It is designed to be consumed through the [Markets](/docs/platform-sdk/markets) package.**

