# Welcome to Explorer

![](/storage/docs/docs/explorer/assets/explorer-intro-header.png)

**ARK's latest Explorer features several improvements that enhance its overall navigability and utility. ARK Explorer 4.0 allows users to access new tools and statistics, monitor transaction data, view individual wallet balances and enjoy an all-new dark theme via a single highly intuitive interface. You can find all the information you need with just a few clicks of a mouse button.**

