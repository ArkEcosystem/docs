# Server Requirements

<x-alert type="success">
If you don't have access to a Linux box you can quickly setup one on [DigitalOcean](https://cloud.digitalocean.com) and other cloud providers.
</x-alert>

| Supported OS | Release Version(s) |
| :----------: | :----------------: |
| **Ubuntu**   | 22.x               |

| Software       | Release Version(s) |
| :------------: | :----------------: |
| **NodeJs**     | >=20.x             |
| **PostgreSQL** | >=15.x             |

| Hardware Specification | Minimum | Recommended   |
| :--------------------: | :-----: | :-----------: |
| **CPUs**               | 2       | 4             |
| **RAM**                | 4GB     | 8GB           |
| **HDD**                | 20GB    | 20GB - 100GB  |
