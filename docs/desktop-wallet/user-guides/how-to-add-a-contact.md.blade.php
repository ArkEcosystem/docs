# How to Add a Contact?

The application provides you a neat interface to add addresses to your contact list. This feature enables you to aggregate all of the addresses which you may transact with or be interested in.

### Adding a Contact

![You can access the contact creation menu by first clicking on the contact icon in the wallet's navigation bar which will expose your contact list.](/storage/docs/docs/desktop-wallet/assets/contacts.png)

![Enter the contact's address and click Next](/storage/docs/docs/desktop-wallet/assets/contactdetails.png)

![(Optional) Enter a name for the contact and click Done](/storage/docs/docs/desktop-wallet/assets/contactconfirmation.png)

![You can now view that contact's activity](/storage/docs/docs/desktop-wallet/assets/contactactivity.png)

![And have access to the contact's activity through the contact interface](/storage/docs/docs/desktop-wallet/assets/contactinterface.png)
